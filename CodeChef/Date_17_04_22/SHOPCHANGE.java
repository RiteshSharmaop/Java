package CodeChef.Apr_22_LongChallenge.Date_17_04_22;
import java.util.*;
public class SHOPCHANGE {
    public static void main(String[] args){
        Scanner sc =new Scanner(System.in);
		int t = sc.nextInt();
		while(t!=0){
		    int x = sc.nextInt();
		    System.out.println(100-x);
		    t--;
		}
        sc.close();
    }
}
